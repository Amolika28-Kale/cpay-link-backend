// models/Notification.js
const mongoose = require('mongoose');

const notificationSchema = new mongoose.Schema({
  user: {
    type: mongoose.Schema.Types.ObjectId,
    ref: 'User',
    required: true
  },
  type: {
    type: String,
    enum: [
      'MISSED_COMMISSION', 
      'LEVEL_UNLOCKED', 
      'LEG_ACTIVATED', 
      'COMMISSION_RECEIVED',
      'NEW_DIRECT_REFERRAL',
      // ✅ ADD THESE NEW TYPES
      'QR_UPDATE_REQUESTED',
      'QR_UPDATED',
      'UTR_REQUESTED'
    ],
    required: true
  },
  title: {
    type: String,
    required: true
  },
  message: {
    type: String,
    required: true
  },
  data: {
    type: mongoose.Schema.Types.Mixed,
    default: {}
  },
  read: {
    type: Boolean,
    default: false
  },
  createdAt: {
    type: Date,
    default: Date.now
  }
});

module.exports = mongoose.model('Notification', notificationSchema);